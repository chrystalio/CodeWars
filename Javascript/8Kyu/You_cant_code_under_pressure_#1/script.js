function doubleInteger(i) {
    // i will be an integer. Double it and return it.
    return i*2;
}

/* Explained Solutions

This function takes an integer as input (i), inside the function there's a single return statement.
The return statement multiplies the input integer i by 2 using the * operator.
The doubled value of the integer is then returned as the output.
So, the function doubleInteger takes an integer as input and returns its doubled value as the output.

*/